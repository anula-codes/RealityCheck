def visualise_predictions(generator, model, class_names, n=12):
    generator.reset()
    images, labels = next(generator)
    preds = model.predict(images[:n], verbose=0)

    fig, axes = plt.subplots(2, 6, figsize=(18, 7))
    fig.suptitle("Test Image Predictions  (green = correct, red = wrong)", fontsize=13)
    for i, ax in enumerate(axes.flat):
        ax.imshow(images[i])
        pred_label  = class_names[int(preds[i] > 0.5)]
        true_label  = class_names[int(labels[i])]
        confidence  = preds[i][0] if preds[i] > 0.5 else 1 - preds[i][0]
        color = "green" if pred_label == true_label else "red"
        ax.set_title(f"True: {true_label}\nPred: {pred_label} ({confidence:.0%})",
                     color=color, fontsize=8)
        ax.axis("off")
    plt.tight_layout()
    plt.savefig("/content/prediction_samples.png", dpi=150)
    plt.show()

visualise_predictions(test_generator, model, CLASS_NAMES)

