def remove_corrupt_images(directory):
    removed = 0
    for root, _, files in os.walk(directory):
        for fname in files:
            fpath = os.path.join(root, fname)
            try:
                with Image.open(fpath) as img:
                    img.verify()
            except Exception:
                print(f"  Removing corrupt file: {fpath}")
                os.remove(fpath)
                removed += 1
    return removed
